/// Generate all Ctr_Sugar lemmas for the datatypes in source.
pub fn generate_ctr_sugar_lemmas(source: &str) -> Vec<ParsedLemma> {
    let mut lemmas = Vec::new();
    for dt in &crate::hol::hol_loader::parse_datatypes(source) {
        let sugar = CtrSugar::from_datatype(dt);
        let theorems = sugar.generate_all_theorems();
        // Convert CtrSugarTheorems to ParsedLemma list
        let dt_name = &sugar.datatype.name;

        // Exhaustion
        lemmas.push(ParsedLemma {
            name: format!("{dt_name}.exhaust"),
            attributes: vec!["elim".into(), "ctr_sugar".into()],
            theorem: Arc::new(theorems.exhaust.clone()),
            proof_script: None,
            alias_for: None,
            source_loc: None,
        });

        // Injectivity
        for (i, thm) in theorems.injects.iter().enumerate() {
            lemmas.push(ParsedLemma {
                name: format!("{dt_name}.inject_{}", i + 1),
                attributes: vec!["simp".into(), "ctr_sugar".into()],
                theorem: Arc::new(thm.clone()),
                proof_script: None,
                alias_for: None,
                source_loc: None,
            });
        }

        // Distinctness
        for (i, thm) in theorems.distincts.iter().enumerate() {
            lemmas.push(ParsedLemma {
                name: format!("{dt_name}.distinct_{}", i + 1),
                attributes: vec!["simp".into(), "ctr_sugar".into()],
                theorem: Arc::new(thm.clone()),
                proof_script: None,
                alias_for: None,
                source_loc: None,
            });
        }

        // Case equations
        for (i, thm) in theorems.case_thms.iter().enumerate() {
            lemmas.push(ParsedLemma {
                name: format!("{dt_name}.case_{}", i + 1),
                attributes: vec!["simp".into(), "ctr_sugar".into()],
                theorem: Arc::new(thm.clone()),
                proof_script: None,
                alias_for: None,
                source_loc: None,
            });
        }

        // Case congruence
        lemmas.push(ParsedLemma {
            name: format!("{dt_name}.case_cong"),
            attributes: vec!["cong".into(), "ctr_sugar".into()],
            theorem: Arc::new(theorems.case_cong),
            proof_script: None,
            alias_for: None,
            source_loc: None,
        });

        // Split
        lemmas.push(ParsedLemma {
            name: format!("{dt_name}.split"),
            attributes: vec!["split".into(), "ctr_sugar".into()],
            theorem: Arc::new(theorems.split),
            proof_script: None,
            alias_for: None,
            source_loc: None,
        });

        // Discriminator theorems
        let disc_thm_idx: usize = 0;
        for (_i, (_ctor, _args)) in sugar.datatype.constructors.iter().enumerate() {
            for (j, (_other_ctor, _other_args)) in sugar.datatype.constructors.iter().enumerate() {
                if disc_thm_idx < theorems.disc_thms.len() {
                    let thm = &theorems.disc_thms[disc_thm_idx];
                    let is_true = _i == j;
                    let suffix = if is_true {
                        format!("disc_{}_true", _i + 1)
                    } else {
                        format!("disc_{}_false_{}", _i + 1, j + 1)
                    };
                    lemmas.push(ParsedLemma {
                        name: format!("{dt_name}.{suffix}"),
                        attributes: vec!["simp".into(), "ctr_sugar".into()],
                        theorem: Arc::new(thm.clone()),
                        proof_script: None,
                        alias_for: None,
                        source_loc: None,
                    });
                }
            }
        }

        // Selector theorems
        let mut sel_idx = 0;
        for (_i, (_ctor, args)) in sugar.datatype.constructors.iter().enumerate() {
            for (j, _) in args.iter().enumerate() {
                if sel_idx < theorems.sel_thms.len() {
                    let thm = &theorems.sel_thms[sel_idx];
                    lemmas.push(ParsedLemma {
                        name: format!("{dt_name}.sel_{}_{}", _i + 1, j + 1),
                        attributes: vec!["simp".into(), "ctr_sugar".into()],
                        theorem: Arc::new(thm.clone()),
                        proof_script: None,
                        alias_for: None,
                        source_loc: None,
                    });
                    sel_idx += 1;
                }
            }
        }

        // Nchotomy
        lemmas.push(ParsedLemma {
            name: format!("{dt_name}.nchotomy"),
            attributes: vec!["elim".into(), "ctr_sugar".into()],
            theorem: Arc::new(theorems.exhaust),
            proof_script: None,
            alias_for: None,
            source_loc: None,
        });
    }
    lemmas
}

