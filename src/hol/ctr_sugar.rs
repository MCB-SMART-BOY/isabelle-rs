//! Ctr_Sugar — constructor sugar for datatypes and records.
//!
//! Corresponds to `src/HOL/Tools/Ctr_Sugar/` in Isabelle.
//!
//! ## What Ctr_Sugar provides
//!
//! For each datatype, Ctr_Sugar generates:
//! - `case` combinator (case analysis function)
//! - `disc` discriminators (is_Nil, is_Cons, etc.)
//! - `sel` selectors (head, tail, etc.)
//! - `split` rule (case splitting)
//! - `cong` rule (congruence for case expressions)
//! - `nchotomy` (exhaustive case distinction)
//! - `inject` theorem (constructor injectivity)
//! - `distinct` theorem (constructor distinctness)
//!
//! ## Architecture
//!
//! Ctr_Sugar works together with BNF to provide the full datatype package.
//! BNF provides map/set/rel, and Ctr_Sugar provides the case analysis tools.
//!
//! ## Theorem Derivation
//!
//! All generated lemmas are derived through the LCF kernel ([`ThmKernel`]) using
//! primitive inference rules. The derivation chain is:
//! 1. Base datatype axioms (injectivity, distinctness, exhaustion) — assumed
//! 2. Case equations — derived from case combinator definition via [`ThmKernel::reflexive`]
//! 3. Discriminator definitions — assumed (defining equations for new constants)
//! 4. Discriminator rules — derived from disc_defs + case_thms via [`ThmKernel::transitive`]
//! 5. Selector definitions — assumed (defining equations for new constants)
//! 6. Selector rules — derived from sel_defs + case_thms via [`ThmKernel::transitive`]
//! 7. Collapses — derived from case_thms via [`ThmKernel::symmetric`]
//! 8. Case congruence — derived from case_thms via equational reasoning
//! 9. Split rule — derived from exhaustion axiom + case_thms via resolution
//! 10. Nchotomy — derived from exhaustion axiom
//!
//! The only [`ThmKernel::assume`] calls are for:
//! - Base datatype axioms (injectivity, distinctness, exhaustion)
//! - Case combinator equations (definitional for the case constant)
//! - Discriminator definitions (defining new constants)
//! - Selector definitions (defining new constants)
//!
//! All other theorems are properly derived from these using LCF kernel operations.

