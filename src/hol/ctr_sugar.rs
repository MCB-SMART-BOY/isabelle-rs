    fn generate_case_lemmas(&self) -> Vec<ParsedLemma> {
        let mut lemmas = Vec::new();
        let case_name = format!("case_{}", self.datatype.name);
        let dt_typ = self.datatype_typ();
        let result_typ = Typ::free("'b", crate::core::types::Sort::singleton("type"));

        let mut case_typ = dt_typ.clone();
        for (_ctor_name, args) in self.datatype.constructors.iter().rev() {
            let branch_typ = if args.is_empty() {
                result_typ.clone()
            } else {
                let mut f: Typ = result_typ.clone();
                for (_sel_opt, arg_typ_str) in args.iter().rev() {
                    let arg_typ = self.parse_arg_type(arg_typ_str);
                    f = Typ::arrow(arg_typ, f);
                }
                f
            };
            case_typ = Typ::arrow(branch_typ, case_typ);
        }

        for (i, (_ctor_name, args)) in self.datatype.constructors.iter().enumerate() {
            let arg_vars: Vec<String> =
                args.iter().enumerate().map(|(j, _)| format!("x{}", j + 1)).collect();
            let ctor_call = self.mk_ctor_term(i, &arg_vars);

            let func_vars: Vec<Term> = (0..self.datatype.constructors.len())
                .map(|j| {
                    let branch = &self.datatype.constructors[j];
                    let f_typ = if branch.1.is_empty() {
                        result_typ.clone()
                    } else {
                        let mut f: Typ = result_typ.clone();
                        for (_sel_opt, arg_typ_str) in branch.1.iter().rev() {
                            let arg_typ = self.parse_arg_type(arg_typ_str);
                            f = Typ::arrow(arg_typ, f);
                        }
                        f
                    };
                    Term::free(format!("f{}", j + 1).as_str(), f_typ)
                })
                .collect();

            let mut case_lhs = Term::const_(case_name.as_str(), case_typ.clone());
            case_lhs = Term::app(case_lhs, ctor_call);
            for f in &func_vars {
                case_lhs = Term::app(case_lhs, f.clone());
            }

            let mut case_rhs = func_vars[i].clone();
            for (j, arg_name) in arg_vars.iter().enumerate() {
                let arg_typ = self.mk_arg_type(i, j);
                case_rhs = Term::app(case_rhs, Term::free(arg_name.as_str(), arg_typ));
            }

            let case_eq = self.mk_eq(case_lhs, case_rhs);
            let thm = self.assume_term(case_eq);

            lemmas.push(self.make_lemma(
                format!("{}.case_{}", self.datatype.name, i + 1),
                vec!["simp".to_string(), "ctr_sugar".to_string()],
                thm,
            ));
        }
        lemmas
    }

    fn generate_disc_lemmas(&self) -> Vec<ParsedLemma> {
        let mut lemmas = Vec::new();
        let bool_typ = Typ::base("bool");

        for (i, (_ctor_name, args)) in self.datatype.constructors.iter().enumerate() {
            let disc_name = &self.discs[i];
            let disc_typ = Typ::arrow(self.datatype_typ(), bool_typ.clone());
            let arg_vars: Vec<String> =
                args.iter().enumerate().map(|(j, _)| format!("x{}", j + 1)).collect();
            let ctor_call = self.mk_ctor_term(i, &arg_vars);

            let disc_app = Term::app(Term::const_(disc_name.as_str(), disc_typ.clone()), ctor_call);
            let true_eq = self.mk_eq(disc_app, self.mk_true());
            let thm = self.assume_term(true_eq);
            lemmas.push(self.make_lemma(
                format!("{}.disc_{}_true", self.datatype.name, i + 1),
                vec!["simp".to_string(), "ctr_sugar".to_string()],
                thm,
            ));

            for (j, (_other_ctor, other_args)) in self.datatype.constructors.iter().enumerate() {
                if i == j {
                    continue;
                }
                let other_vars: Vec<String> =
                    other_args.iter().enumerate().map(|(k, _)| format!("y{}", k + 1)).collect();
                let other_call = self.mk_ctor_term(j, &other_vars);
                let disc_app2 =
                    Term::app(Term::const_(disc_name.as_str(), disc_typ.clone()), other_call);
                let false_eq = self.mk_eq(disc_app2, self.mk_false());
                let thm = self.assume_term(false_eq);
                lemmas.push(self.make_lemma(
                    format!("{}.disc_{}_false_{}", self.datatype.name, i + 1, j + 1),
                    vec!["simp".to_string(), "ctr_sugar".to_string()],
                    thm,
                ));
            }
        }
        lemmas
    }

    fn generate_sel_lemmas(&self) -> Vec<ParsedLemma> {
        let mut lemmas = Vec::new();

        for (i, (_ctor_name, args)) in self.datatype.constructors.iter().enumerate() {
            let sel_names = &self.sels[i];
            let arg_vars: Vec<String> =
                args.iter().enumerate().map(|(j, _)| format!("x{}", j + 1)).collect();
            let ctor_call = self.mk_ctor_term(i, &arg_vars);

            for (j, sel_name) in sel_names.iter().enumerate() {
                let arg_typ = self.mk_arg_type(i, j);
                let sel_typ = Typ::arrow(self.datatype_typ(), arg_typ.clone());
                let sel_app =
                    Term::app(Term::const_(sel_name.as_str(), sel_typ), ctor_call.clone());
                let arg_var = Term::free(arg_vars[j].as_str(), arg_typ);
                let sel_eq = self.mk_eq(sel_app, arg_var);
                let thm = self.assume_term(sel_eq);

                lemmas.push(self.make_lemma(
                    format!("{}.sel_{}_{}", self.datatype.name, i + 1, j + 1),
                    vec!["simp".to_string(), "ctr_sugar".to_string()],
                    thm,
                ));
            }
        }
        lemmas
    }

    fn generate_split_lemma(&self) -> ParsedLemma {
        let split_name = format!("{}.split", self.datatype.name);
        let dt_typ = self.datatype_typ();
        let case_name = format!("case_{}", self.datatype.name);
        let prop_typ = Typ::base("prop");
        let x = Term::free("x", dt_typ.clone());

        let mut disj_parts: Vec<Term> = Vec::new();
        for (i, (_ctor_name, args)) in self.datatype.constructors.iter().enumerate() {
            let arg_vars: Vec<Term> = args
                .iter()
                .enumerate()
                .map(|(j, _)| {
                    let arg_typ = self.mk_arg_type(i, j);
                    Term::free(format!("x{}", j + 1).as_str(), arg_typ)
                })
                .collect();

            let ctor_app = self.mk_ctor_term(
                i,
                &arg_vars
                    .iter()
                    .enumerate()
                    .map(|(j, _)| format!("x{}", j + 1))
                    .collect::<Vec<_>>(),
            );

            let eq = self.mk_eq(x.clone(), ctor_app);
            let f_i = Term::free(format!("f{}", i + 1).as_str(), prop_typ.clone());
            let app = if args.is_empty() {
                f_i
            } else {
                let mut f_app = f_i;
                for v in &arg_vars {
                    f_app = Term::app(f_app, v.clone());
                }
                f_app
            };

            let imp = Pure::mk_implies(eq, app);
            let mut forall = imp;
            for (j, _v) in arg_vars.iter().enumerate().rev() {
                forall =
                    Pure::mk_all(format!("x{}", j + 1).as_str(), self.mk_arg_type(i, j), forall);
            }
            disj_parts.push(forall);
        }

        let split_formula = if disj_parts.is_empty() {
            // No constructors → split is trivially true
            Pure::mk_implies(Term::free("P", Typ::base("bool")), Term::free("P", Typ::base("bool")))
        } else if disj_parts.len() == 1 {
            disj_parts.pop().unwrap()
        } else {
            let mut conj = disj_parts.pop().unwrap();
            while let Some(part) = disj_parts.pop() {
                conj = Term::app(
                    Term::app(
                        Term::const_(
                            "HOL.conj",
                            Typ::arrow(
                                Typ::base("bool"),
                                Typ::arrow(Typ::base("bool"), Typ::base("bool")),
                            ),
                        ),
                        part,
                    ),
                    conj,
                );
            }
            conj
        };

        self.make_lemma(
            split_name,
            vec!["split".to_string(), "ctr_sugar".to_string()],
            self.assume_term(split_formula),
        )
    }

    fn generate_cong_lemma(&self) -> ParsedLemma {
        let cong_name = format!("{}.case_cong", self.datatype.name);
        let dt_typ = self.datatype_typ();
        let case_name = format!("case_{}", self.datatype.name);
        let result_typ = Typ::free("'b", crate::core::types::Sort::singleton("type"));

        let m = Term::free("M", dt_typ.clone());
        let m_prime = Term::free("M'", dt_typ.clone());

        let mut funcs: Vec<Term> = Vec::new();
        let mut funcs_prime: Vec<Term> = Vec::new();
        for (i, (_ctor, args)) in self.datatype.constructors.iter().enumerate() {
            let f_typ = if args.is_empty() {
                result_typ.clone()
            } else {
                let mut f_typ: Typ = result_typ.clone();
                for (_sel, arg_typ_str) in args.iter().rev() {
                    let arg_typ = self.parse_arg_type(arg_typ_str);
                    f_typ = Typ::arrow(arg_typ, f_typ);
                }
                f_typ
            };
            funcs.push(Term::free(format!("f{}", i + 1).as_str(), f_typ.clone()));
            funcs_prime.push(Term::free(format!("f{}'", i + 1).as_str(), f_typ));
        }

        let mut case_lhs = Term::const_(case_name.as_str(), dt_typ.clone());
        case_lhs = Term::app(case_lhs, m.clone());
        for f in &funcs {
            case_lhs = Term::app(case_lhs, f.clone());
        }

        let mut case_rhs = Term::const_(case_name.as_str(), dt_typ.clone());
        case_rhs = Term::app(case_rhs, m_prime.clone());
        for f in &funcs_prime {
            case_rhs = Term::app(case_rhs, f.clone());
        }

        let conclusion = self.mk_eq(case_lhs, case_rhs);
        let hyp1 = self.mk_eq(m.clone(), m_prime.clone());

        let mut all_hyps = vec![hyp1];
        for (i, (_ctor, args)) in self.datatype.constructors.iter().enumerate() {
            let arg_vars: Vec<Term> = args
                .iter()
                .enumerate()
                .map(|(j, _)| Term::free(format!("x{}", j + 1).as_str(), Typ::dummy()))
                .collect();
            let mut f_i_app = funcs[i].clone();
            let mut f_i_prime_app = funcs_prime[i].clone();
            for v in &arg_vars {
                f_i_app = Term::app(f_i_app, v.clone());
                f_i_prime_app = Term::app(f_i_prime_app, v.clone());
            }
            let arg_eq = self.mk_eq(f_i_app, f_i_prime_app);
            let mut forall = Pure::mk_implies(
                self.mk_eq(Term::free("x", dt_typ.clone()), m_prime.clone()),
                arg_eq,
            );
            for (j, _v) in arg_vars.iter().enumerate().rev() {
                forall = Pure::mk_all(format!("x{}", j + 1).as_str(), Typ::dummy(), forall);
            }
            all_hyps.push(forall);
        }

        let mut cong_term = conclusion;
        for hyp in all_hyps.iter().rev() {
            cong_term = Pure::mk_implies(hyp.clone(), cong_term);
        }

        self.make_lemma(
            cong_name,
            vec!["cong".to_string(), "ctr_sugar".to_string()],
            self.assume_term(cong_term),
        )
    }

    fn generate_nchotomy_lemma(&self) -> ParsedLemma {
        let nchotomy_name = format!("{}.nchotomy", self.datatype.name);
        let dt_typ = self.datatype_typ();
        let x = Term::free("x", dt_typ.clone());

        let mut disj_parts: Vec<Term> = Vec::new();
        for (i, (_ctor_name, args)) in self.datatype.constructors.iter().enumerate() {
            let arg_vars: Vec<Term> = args
                .iter()
                .enumerate()
                .map(|(j, _)| Term::free(format!("x{}", j + 1).as_str(), Typ::dummy()))
                .collect();
            let ctor_app = self.mk_ctor_term(
                i,
                &arg_vars
                    .iter()
                    .enumerate()
                    .map(|(j, _)| format!("x{}", j + 1))
                    .collect::<Vec<_>>(),
            );
            let eq = self.mk_eq(x.clone(), ctor_app);
            disj_parts.push(eq);
        }

        let nchotomy = if disj_parts.is_empty() {
            // No constructors → nchotomy is trivially true
            Term::const_("HOL.True", Typ::base("bool"))
        } else if disj_parts.len() == 1 {
            disj_parts.pop().unwrap()
        } else {
            let mut disj = disj_parts.pop().unwrap();
            while let Some(part) = disj_parts.pop() {
                disj = Term::app(
                    Term::app(
                        Term::const_(
                            "HOL.disj",
                            Typ::arrow(
                                Typ::base("bool"),
                                Typ::arrow(Typ::base("bool"), Typ::base("bool")),
                            ),
                        ),
                        part,
                    ),
                    disj,
                );
            }
            disj
        };

        let nchotomy = Pure::mk_all("x", dt_typ, nchotomy);
        self.make_lemma(
            nchotomy_name,
            vec!["elim".to_string(), "ctr_sugar".to_string()],
            self.assume_term(nchotomy),
        )
    }

    fn generate_size_lemmas(&self) -> Vec<ParsedLemma> {
        let mut lemmas = Vec::new();
        let size_name = format!("size_{}", self.datatype.name);
        let dt_typ = self.datatype_typ();
        let nat_typ = Typ::base("nat");
        let size_typ = Typ::arrow(dt_typ.clone(), nat_typ.clone());

        let plus_const = Term::const_(
            "Groups.plus",
            Typ::arrow(nat_typ.clone(), Typ::arrow(nat_typ.clone(), nat_typ.clone())),
        );
        let one = Term::const_("Groups.one", nat_typ.clone());

        for (ctor_idx, (_ctor_name, args)) in self.datatype.constructors.iter().enumerate() {
            let arg_vars: Vec<String> =
                args.iter().enumerate().map(|(j, _)| format!("x{}", j + 1)).collect();
            let ctor_call = self.mk_ctor_term(ctor_idx, &arg_vars);

            let size_app = Term::app(Term::const_(size_name.as_str(), size_typ.clone()), ctor_call);

            let mut rhs = one.clone();
            for (j, arg_name) in arg_vars.iter().enumerate() {
                let arg_typ = self.mk_arg_type(ctor_idx, j);
                let arg_var = Term::free(arg_name.as_str(), arg_typ.clone());
                let sz = if arg_typ == dt_typ {
                    Term::app(Term::const_(size_name.as_str(), size_typ.clone()), arg_var)
                } else {
                    let nested_size = format!("size_{}", arg_typ);
                    Term::app(
                        Term::const_(nested_size.as_str(), Typ::arrow(arg_typ, nat_typ.clone())),
                        arg_var,
                    )
                };
                rhs = Term::app(Term::app(plus_const.clone(), rhs), sz);
            }

            let size_eq = self.mk_eq(size_app, rhs);
            let thm = self.assume_term(size_eq);

            lemmas.push(self.make_lemma(
                format!("{}.size_{}", self.datatype.name, ctor_idx + 1),
                vec!["simp".to_string(), "ctr_sugar".to_string()],
                thm,
            ));
        }
        lemmas
    }

    fn generate_inject_lemmas(&self) -> Vec<ParsedLemma> {
        self.inject_axioms
            .iter()
            .enumerate()
            .map(|(i, thm)| {
                self.make_lemma(
                    format!("{}.inject_{}", self.datatype.name, i + 1),
                    vec!["simp".to_string(), "ctr_sugar".to_string()],
                    Arc::clone(thm),
                )
            })
            .collect()
    }

    fn generate_distinct_lemmas(&self) -> Vec<ParsedLemma> {
        self.distinct_axioms
            .iter()
            .enumerate()
            .map(|(i, thm)| {
                self.make_lemma(
                    format!("{}.distinct_{}", self.datatype.name, i + 1),
                    vec!["simp".to_string(), "ctr_sugar".to_string()],
                    Arc::clone(thm),
                )
            })
            .collect()
    }

    fn generate_exhaustion_lemma(&self) -> ParsedLemma {
        self.make_lemma(
            format!("{}.exhaust", self.datatype.name),
            vec!["elim".to_string(), "ctr_sugar".to_string()],
            Arc::clone(&self.exhaustion_axiom),
        )
    }
