    /// Construct injectivity axioms: `Ctor_i args = Ctor_i args' ==> args = args'`.
    fn mk_inject_axioms(def: &DatatypeDef) -> Vec<Arc<Thm>> {
        let mut axioms = Vec::new();
        let dt_typ = Typ::base(def.name.as_str());

        for (ctor_idx, (ctor_name, args)) in def.constructors.iter().enumerate() {
            if args.is_empty() {
                let lhs = Term::const_(ctor_name.as_str(), dt_typ.clone());
                let eq = Pure::mk_equals(dt_typ.clone(), lhs.clone(), lhs);
                let ct = CTerm::certify(eq);
                axioms.push(Arc::new(ThmKernel::reflexive(ct)));
                continue;
            }

            let mut lhs_args: Vec<Term> = Vec::new();
            let mut rhs_args: Vec<Term> = Vec::new();
            for (j, (_sel, _arg_typ)) in args.iter().enumerate() {
                lhs_args.push(Term::free(format!("x{}", j + 1).as_str(), Typ::dummy()));
                rhs_args.push(Term::free(format!("y{}", j + 1).as_str(), Typ::dummy()));
            }

            let mut lhs_term = Term::const_(ctor_name.as_str(), dt_typ.clone());
            for a in &lhs_args {
                lhs_term = Term::app(lhs_term, a.clone());
            }
            let mut rhs_term = Term::const_(ctor_name.as_str(), dt_typ.clone());
            for a in &rhs_args {
                rhs_term = Term::app(rhs_term, a.clone());
            }

            let eq_hyp = Pure::mk_equals(dt_typ.clone(), lhs_term, rhs_term);

            let mut concl = hologic::true_const();
            for j in (0..lhs_args.len()).rev() {
                let arg_eq =
                    Pure::mk_equals(Typ::dummy(), lhs_args[j].clone(), rhs_args[j].clone());
                if j == lhs_args.len() - 1 {
                    concl = arg_eq;
                } else {
                    concl = Term::app(
                        Term::app(
                            Term::const_(
                                "HOL.conj",
                                Typ::arrow(
                                    Typ::base("bool"),
                                    Typ::arrow(Typ::base("bool"), Typ::base("bool")),
                                ),
                            ),
                            arg_eq,
                        ),
                        concl,
                    );
                }
            }

            let inject_term = Pure::mk_implies(eq_hyp, concl);
            axioms.push(Arc::new(ThmKernel::assume(CTerm::certify(inject_term))));
        }
        axioms
    }

    /// Construct distinctness axioms: `Ctor_i args /= Ctor_j args'` for i < j.
    fn mk_distinct_axioms(def: &DatatypeDef) -> Vec<Arc<Thm>> {
        let mut axioms = Vec::new();
        let dt_typ = Typ::base(def.name.as_str());

        for i in 0..def.constructors.len() {
            for j in (i + 1)..def.constructors.len() {
                let (ctor_i, args_i) = &def.constructors[i];
                let (ctor_j, args_j) = &def.constructors[j];

                let mut lhs = Term::const_(ctor_i.as_str(), dt_typ.clone());
                for (k, _) in args_i.iter().enumerate() {
                    lhs = Term::app(lhs, Term::free(format!("x{}", k + 1).as_str(), Typ::dummy()));
                }
                let mut rhs = Term::const_(ctor_j.as_str(), dt_typ.clone());
                for (k, _) in args_j.iter().enumerate() {
                    rhs = Term::app(rhs, Term::free(format!("y{}", k + 1).as_str(), Typ::dummy()));
                }

                let eq = Pure::mk_equals(dt_typ.clone(), lhs, rhs);
                let false_term = hologic::false_const();
                let distinct = Pure::mk_implies(eq, false_term);
                axioms.push(Arc::new(ThmKernel::assume(CTerm::certify(distinct))));
            }
        }
        axioms
    }

    /// Construct the exhaustion axiom: every value of the datatype is one of the constructors.
    fn mk_exhaustion_axiom(def: &DatatypeDef) -> Arc<Thm> {
        let dt_typ = Typ::base(def.name.as_str());
        let x = Term::free("x", dt_typ.clone());
        let p = Term::var("P", 0, Typ::arrow(dt_typ.clone(), Typ::base("bool")));

        let mut impl_chain = Term::app(p.clone(), x);

        for (ctor_name, args) in def.constructors.iter().rev() {
            let mut ctor_call = Term::const_(ctor_name.as_str(), dt_typ.clone());
            for (k, _) in args.iter().enumerate() {
                let arg_var = Term::var(format!("x{}", k + 1).as_str(), 0, Typ::dummy());
                ctor_call = Term::app(ctor_call, arg_var);
            }
            let hypothesis = Term::app(p.clone(), ctor_call);
            let forall_hyp = Self::mk_forall_vars(hypothesis, args.len());
            impl_chain = Pure::mk_implies(forall_hyp, impl_chain);
        }

        let result = Pure::mk_all("P", Typ::arrow(dt_typ.clone(), Typ::base("bool")), impl_chain);
        Arc::new(ThmKernel::assume(CTerm::certify(result)))
    }

    /// Wrap a term in universal quantifiers for a given number of variables.
    fn mk_forall_vars(term: Term, num_vars: usize) -> Term {
        let mut result = term;
        for k in (0..num_vars).rev() {
            result = Pure::mk_all(format!("x{}", k + 1).as_str(), Typ::dummy(), result);
        }
        result
    }

