    /// Generate all Ctr_Sugar lemmas for this datatype (legacy interface).
    pub fn generate_lemmas(&self) -> Vec<ParsedLemma> {
        let mut lemmas = Vec::new();

        // 0. Injectivity and distinctness axioms (base theorems)
        lemmas.extend(self.generate_inject_lemmas());
        lemmas.extend(self.generate_distinct_lemmas());
        lemmas.push(self.generate_exhaustion_lemma());

        // 1. Case combinator equations
        lemmas.extend(self.generate_case_lemmas());

        // 2. Discriminator rules
        lemmas.extend(self.generate_disc_lemmas());

        // 3. Selector rules
        lemmas.extend(self.generate_sel_lemmas());

        // 4. Split rule
        lemmas.push(self.generate_split_lemma());

        // 5. Congruence rule
        lemmas.push(self.generate_cong_lemma());

        // 6. Nchotomy (exhaustive case distinction)
        lemmas.push(self.generate_nchotomy_lemma());

        // 7. Size function
        lemmas.extend(self.generate_size_lemmas());

        lemmas
    }

    /// Build a branch type for a constructor in the case combinator.
    /// Nullary branch: `result_typ`. Non-nullary: `arg1 → ... → argn → result_typ`.
    fn mk_branch_type(&self, args: &[(Option<String>, String)], result_typ: &Typ) -> Typ {
        if args.is_empty() {
            result_typ.clone()
        } else {
            let mut f: Typ = result_typ.clone();
            for (_sel_opt, arg_typ_str) in args.iter().rev() {
                let arg_typ = self.parse_arg_type(arg_typ_str);
                f = Typ::arrow(arg_typ, f);
            }
            f
        }
    }

    /// Construct proper terms for this datatype's type and constructors.
    fn datatype_typ(&self) -> Typ {
        Typ::base(self.datatype.name.as_str())
    }

    /// Build a constructor application term: `Ctor x1 x2 ... xn`.
    fn mk_ctor_term(&self, ctor_idx: usize, arg_names: &[String]) -> Term {
        let ctor_name = &self.datatype.constructors[ctor_idx].0;
        let ctor_typ = self.mk_ctor_fun_type(ctor_idx);
        let mut term = Term::const_(ctor_name.as_str(), ctor_typ);
        for (j, arg_name) in arg_names.iter().enumerate() {
            let arg_typ = self.mk_arg_type(ctor_idx, j);
            term = Term::app(term, Term::free(arg_name.as_str(), arg_typ));
        }
        term
    }

    /// Build a constructor's full function type: arg1_typ -> arg2_typ -> ... -> result_typ.
    fn mk_ctor_fun_type(&self, ctor_idx: usize) -> Typ {
        let args = &self.datatype.constructors[ctor_idx].1;
        let result = self.datatype_typ();
        let mut typ = result;
        for (_sel_opt, arg_typ_str) in args.iter().rev() {
            let arg_typ = self.parse_arg_type(arg_typ_str);
            typ = Typ::arrow(arg_typ, typ);
        }
        typ
    }

    /// Parse an argument type string into a Typ.
    fn parse_arg_type(&self, s: &str) -> Typ {
        if s.starts_with('\'') {
            return Typ::free(s, crate::core::types::Sort::singleton("type"));
        }
        if s.contains(' ') {
            return Typ::base(s);
        }
        Typ::base(s)
    }

    fn mk_arg_type(&self, ctor_idx: usize, arg_idx: usize) -> Typ {
        let args = &self.datatype.constructors[ctor_idx].1;
        if let Some((_sel, arg_typ_str)) = args.get(arg_idx) {
            self.parse_arg_type(arg_typ_str)
        } else {
            Typ::dummy()
        }
    }

    /// Build an equality term: `t = u`.
    fn mk_eq(&self, t: Term, u: Term) -> Term {
        Pure::mk_equals(Typ::base("prop"), t, u)
    }

    /// Build True constant.
    fn mk_true(&self) -> Term {
        hologic::true_const()
    }

    /// Build False constant.
    fn mk_false(&self) -> Term {
        hologic::false_const()
    }

    /// Assume a proposition and return the theorem.
    fn assume_term(&self, t: Term) -> Arc<Thm> {
        Arc::new(ThmKernel::assume(CTerm::certify(t)))
    }

    /// Create a ParsedLemma from a theorem.
    fn make_lemma(&self, name: String, attrs: Vec<String>, thm: Arc<Thm>) -> ParsedLemma {
        ParsedLemma {
            name,
            attributes: attrs,
            theorem: thm,
            proof_script: None,
            alias_for: None,
            source_loc: None,
        }
    }

