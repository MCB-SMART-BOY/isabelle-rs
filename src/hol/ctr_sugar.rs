    #[test]
    fn test_disc_thm_count_option() {
        let dt = make_option_dt();
        let sugar = CtrSugar::from_datatype(&dt);
        let thms = sugar.generate_all_theorems();

        // 2 discriminators × 2 constructors = 4 disc theorems
        assert_eq!(thms.disc_thms.len(), 4, "Expected 4 disc theorems for option");

        // Count True/False
        let mut true_count = 0;
        let mut false_count = 0;
        for dt_thm in &thms.disc_thms {
            let (_lhs, rhs) = Pure::dest_equals(dt_thm.prop().term()).unwrap();
            let rhs_str = format!("{rhs:?}");
            if rhs_str.contains("True") {
                true_count += 1;
            }
            if rhs_str.contains("False") {
                false_count += 1;
            }
        }
        assert_eq!(true_count, 2, "Should have 2 True disc theorems");
        assert_eq!(false_count, 2, "Should have 2 False disc theorems");
    }
